// typecheck

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const registryPath = path.join(__dirname, '..', 'public', 'r', 'registry.json')
const srcRegistryPath = path.join(__dirname, '..', 'src', 'registry')

// Ensure public/r directory exists
const publicDir = path.join(__dirname, '..', 'public')
const registryDir = path.join(publicDir, 'r')

// Clean and recreate public/r directory
if (fs.existsSync(registryDir)) {
  fs.rmSync(registryDir, { recursive: true, force: true })
}
if (!fs.existsSync(publicDir)) {
  fs.mkdirSync(publicDir, { recursive: true })
}
fs.mkdirSync(registryDir, { recursive: true })

// Recursively find all registry-item.json files
function findRegistryItems(dir) {
  const items = []

  const entries = fs.readdirSync(dir, { withFileTypes: true })

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name)

    if (entry.isDirectory()) {
      // Skip node_modules and hidden directories
      if (entry.name === 'node_modules' || entry.name.startsWith('.')) {
        continue
      }
      // Recursively search subdirectories
      items.push(...findRegistryItems(fullPath))
    } else if (entry.name === 'registry-item.json') {
      try {
        const content = fs.readFileSync(fullPath, 'utf-8')
        const item = JSON.parse(content)
        items.push(item)
        console.log(`✓ Found ${item.name || fullPath}`)
      } catch (error) {
        console.error(`Error reading ${fullPath}:`, error)
      }
    }
  }

  return items
}

// Find all registry items
console.log('Scanning registry directory...')
const allItems = findRegistryItems(srcRegistryPath)

console.log(`Found ${allItems.length} registry items`)

// Process registry items to add file contents
const processedItems = allItems
  .filter((item) => item.type !== 'registry:example')
  .map((item) => {
    const processedFiles = item.files.map((file) => {
      const filePath = path.join(__dirname, '..', 'src', 'registry', file.path)

      // Read file content
      let content = ''
      if (fs.existsSync(filePath)) {
        content = fs.readFileSync(filePath, 'utf-8')
      }

      return {
        ...file,
        content,
      }
    })

    // Create individual component JSON file
    const componentJson = {
      ...item,
      files: processedFiles,
    }
    const componentJsonPath = path.join(registryDir, `${item.name}.json`)
    fs.writeFileSync(componentJsonPath, JSON.stringify(componentJson, null, 2))

    return {
      ...item,
      files: processedFiles,
    }
  })

// Create registry object
const registry = {
  name: 'Airiot UI Library',
  homepage: 'https://airiot.cn/ui',
  items: allItems,
}

const cleanedRegistry = {
  $schema: 'https://ui.shadcn.com/schema/registry.json',
  ...registry,
  items: processedItems,
}

fs.writeFileSync(registryPath, JSON.stringify(cleanedRegistry, null, 2))

console.log(`✓ Registry built successfully at ${registryPath}`)

// Create a registry index file for use by ComponentPreview in the app.
const registryIndex = `
// @ts-nocheck
// This file is autogenerated by scripts/build-registry.mjs
// Do not edit this file directly.
import * as React from "react"

export const Index = {
  "default": {
    ${allItems
      .filter((item) => item.type === 'registry:example')
      .map((item) => {
        const componentFile = item.files.find((file) => file.path.endsWith('.tsx'))

        return `
    "${item.name}": {
      component: ${componentFile ? `React.lazy(() => import("@/${componentFile.path}"))` : 'null'},
    }
    `
      }).join('')}
  },
} as const
`

const registryIndexPath = path.join(__dirname, '..', '__registry__', 'index.tsx')
const registryIndexDir = path.dirname(registryIndexPath)

if (!fs.existsSync(registryIndexDir)) {
  fs.mkdirSync(registryIndexDir, { recursive: true })
}

fs.writeFileSync(registryIndexPath, registryIndex)

console.log(`✓ Registry index built successfully at ${registryIndexPath}`)
console.log(`✓ Total items: ${cleanedRegistry.items.length}`)
